import { useStaticQuery, graphql } from "gatsby"
export const useCatalystConfig = () => {
  const { catalystConfig } = useStaticQuery(
    graphql`
      query {
        catalystConfig {
          contentPath
          assetPath
          displaySiteLogo
          displaySiteTitle
          useHero
          invertSiteLogo
          mobileMenuBreakpoint
          useStickyHeader
          useSanity
        }
      }
    `
  )
  return catalystConfig
}
